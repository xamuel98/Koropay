<script>
import { XIcon } from 'vue-feather-icons'

export default {
    components: {XIcon},
    // watch: {
    //     '$route' () {
    //         document.body.style.overflow = 'auto'
    //     }
    // },
    mounted() {
        let panelCloseBtn = document.querySelector('.panel__close');
        let panel = document.querySelector('.panel');
        let panelOpenBtn = document.querySelector('.open-panel');
        let navbar = document.getElementById('site-header');
        let overlay;
        let dropDown = document.querySelector('.mobile-dropdown-click');
        let dropDownMenu = document.querySelector('.mobile-dropdown');


        // Close side panel
        const closePanelFunction = () => {
            if(panel.classList.contains('panel--visible')) {
                panel.classList.remove('panel--visible')
                navbar.classList.remove('hide')
                // document.body.style.overflow = 'auto'

                // Remove overlay
                let backDrop = document.querySelector('.panel--overlay');
                backDrop.remove();

                // Remove show from dropdown
                dropDown.classList.remove('show')
                dropDownMenu.classList.remove('show')
            }
        }

        panelCloseBtn.addEventListener('click', closePanelFunction)

        // Open side panel
        const openPanelFunction = () => {
            if(!panel.classList.contains('panel--visible')) {
                panel.classList.add('panel--visible')
                navbar.classList.add('hide')
                // document.body.style.overflow = 'hidden'

                // Add overlay
                let elem = document.createElement('div');
                elem.className = 'panel--overlay';
                document.body.appendChild(elem);

                // Create an action to click overlay to close panel
                overlay = document.querySelector('.panel--overlay');
                
                if (overlay !== null || overlay !== undefined) {
                    overlay.addEventListener('click', () => {
                        if(panel.classList.contains('panel--visible')) {
                            panel.classList.remove('panel--visible')
                            // document.body.style.overflow = 'auto'
                    
                            // Remove overlay
                            let backDrop = document.querySelector('.panel--overlay');
                            backDrop.remove();
                        }
                    })
                }
            }
        }

        panelOpenBtn.addEventListener('click', openPanelFunction)

        // Dropdown - mobile
        dropDown.addEventListener('click', () => {
            dropDown.classList.toggle('show')
            dropDownMenu.classList.toggle('show')
        })

        // Hide sidebar panel on browser resize
        const browserResized = (x) => {
            if (x.matches || panel.classList.contains('panel--visible')) { // If media query matches
                closePanelFunction()
            }
        }

        let x = window.matchMedia("(max-width: 768px)")
        browserResized(x) // Call listener function at run time
        x.addListener(browserResized) // Attach listener function on state changes
        
        // Navbar
        let doc = document.documentElement;
        let w   = window;

        /*
        define four variables: curScroll, prevScroll, curDirection, prevDirection
        */

        let curScroll;
        let prevScroll = w.scrollY || doc.scrollTop;
        let curDirection = 0;
        let prevDirection = 0;

        /*
        how it works:
        -------------
        create a scroll event listener
        create function to check scroll position on each scroll event,
        compare curScroll and prevScroll values to find the scroll direction
        scroll up - 1, scroll down - 2, initial - 0
        then set the direction value to curDirection
        compare curDirection and prevDirection
        if it is different, call a function to show or hide the header
        example:
        step 1: user scrolls down: curDirection 2, prevDirection 0 > hide header
        step 2: user scrolls down again: curDirection 2, prevDirection 2 > already hidden, do nothing
        step 3: user scrolls up: curDirection 1, prevDirection 2 > show header
        */

        let header = document.getElementById('site-header');
        let toggled;
        let threshold = 200;

        const checkScroll = () => {
            curScroll = w.scrollY || doc.scrollTop;
            navbar.classList.toggle("__shadow", w.scrollY > 0)
            console.log(curScroll, prevScroll);
            if(curScroll > prevScroll) {
                // scrolled down
                curDirection = 2;
            }
            else {
                //scrolled up
                curDirection = 1;
            }

            if(curDirection !== prevDirection) {
                toggled = toggleHeader();
            }

            prevScroll = curScroll;
            if(toggled) {
                prevDirection = curDirection;
            }
        };

        const toggleHeader = () => { 
            toggled = true;
            if(curDirection === 2 && curScroll > threshold) {
                header.classList.add('hide');
            }
            else if (curDirection === 1) {
                header.classList.remove('hide');
            }
            else {
                toggled = false;
            }
            return toggled;
        };

        window.addEventListener('scroll', checkScroll);
    }
}
